/// Available visual themes for the Smivo app.
///
/// Each variant defines a complete set of design tokens (colors, radii,
/// typography, shadows) that are injected into the widget tree via
/// [ThemeExtension].
enum SmivoThemeVariant {
  /// Original teal/cyan brand theme with rounded corners and bright accents.
  teal,

  /// IKEA-inspired flat theme: deep blue + yellow, sharp corners,
  /// no divider lines, ambient shadows.
  ikea,
}
